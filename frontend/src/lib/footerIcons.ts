export const footerPresetIconFor = (
  iconUrl: string,
): ComponentType<{ size?: number }> | null =>
  iconUrl.startsWith(PRESET_ICON_PREFIX)
    ? (FOOTER_ICON_PRESETS[iconUrl.slice(PRESET_ICON_PREFIX.length)] ?? null)
    : null;
