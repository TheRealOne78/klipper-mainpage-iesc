/** `footer_links[].icon_url` values starting with this prefix pick a bundled
 * icon instead of an admin-uploaded image — no upload needed for common
 * links like GitHub (lucide-react ships no brand icons, so `GitFork` stands
 * in for source-code/repository links). */
export const PRESET_ICON_PREFIX = "icon:";

